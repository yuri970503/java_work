package basic.datatype;

public class IntegerExample {

	public static void main(String[] args) {
		
		/*
		 * # 리터널: 변수에 저장되기 전의 상수값.
		 * 자마의 정수 리터널의 타입은 int 입니다.
		 * int보다 더 큰 범위의 수를 표현하기 위해서는
		 * long을 사용합니다 
		 */
		
		
		byte a = 127;
	// 타입   변수  상수 - 항상 그 수를 나타내는 수 
		
		// 8bit
		//-2^7 ~ 2^7 -1
		short b = 32767;
		int c = 2147483647;
		long d = 2147483648L; //long을 표현할때는 값의 뒤에 L을 붙여준다
		
		
		
		

	}

}
